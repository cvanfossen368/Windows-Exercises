#These operators compare the value on either side of them and decide..
#..the relation among them. They are also called Relational operators.

#Assume variable a holds the value 10 and variable b holds the variable 20, then -
a = 10
b = 20

# == : If the values of two operands are equal, then the condition becomes true.
# (a == b) is not true.
print(a == b)

# != : If values of two operands are not equal, then condition becomes true.
# (a != b) is true.
print(a != b)

# > : If the value of left operand is greater than the value of right operand,
#then condition becomes true.
# (a > b) is not true.
print(a > b)

# < : If the value of left operand is less than the value of right operand, then..
#..condition becomes true.
# (a < b) is true.
print(a < b)

# >= : If the value of left operand is greater than or equal to the value of right..
#..operand, then condition becomes true.
# (a >= b) is not true.
print(a >= b)

# <= : If the value of left operand is less than or equal to the value of right..
#..operand, then condition becomes true.
# (a <= b) is true.
print(a <= b)