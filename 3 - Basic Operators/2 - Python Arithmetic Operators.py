#Assume variable a holds the value 10 and variable b holds the value 21, then -
a = 10
b = 21
# + Addition: Adds value on either side of the operator
# a + b = 31
print(a + b)

# - Subtraction: Subtracts right hand operand from left hand operand.
# a - b = -11
print(a - b)

# * Multiplication: Multiplies values on either side of the operator.
# a * B = 210
print(a * b)

# / Division: Divides left hand operand by right hand operand.
# b / a = 2.1
print(b / a)

# % Modulus: Divides left hand operand by right hand operand and returns remainder.
# b % a = 1
print(b % a)

#** Exponent: Performs exponential (power) calculations on operators
#a**b = 10 to the power 20
print(a**b)

# // Floor Division: The division of operands where the result is the quotient..
#..in which the digits after the decimal point are removed. But if one of the..
#..operands is negative, the result is floored, i.e., rounded away from zero..
#..(towards negative infinity)
#b // a = 2
print(b // a)
print(-b // -a)