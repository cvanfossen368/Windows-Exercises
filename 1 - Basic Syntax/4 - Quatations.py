#Single('), double("), and triple (""") quotes are accepted to denote..
#..string literals, as long as the same type of quote starts and..
#..ends the string.
word = 'word'
sentence = "This is a sentence."
#Triple quotes are used to span the string across multiple lines.
paragraph = """This is a paragraph. It is
made up of multiple lines and sentences."""
#print, word, sentence, and paragraph
print(word)
print(sentence)
print(paragraph)